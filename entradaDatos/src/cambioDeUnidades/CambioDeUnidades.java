package cambioDeUnidades;

public class CambioDeUnidades {
	
	public static void main(String[] args) {
	
	double metros = 5 ;
	double centimetros = metros * 100 ;
	double milimetros = metros * 1000;
	double decimetros = metros * 10 ;
	
	System.out.println("Los Metros son: " + metros + "\nLos centimetros son: " + centimetros + "cm" + "\nLos milimetros son: " + milimetros + "mm" + "\nLos decimetros son: " + decimetros);
	
	
	
	
	}

}
